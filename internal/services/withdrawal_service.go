package services

import (
	"context"
	"database/sql"
	"errors"
	"time"
)

func RequestWithdrawal(db *sql.DB, userID int, amount float64) error {

	//////////////////////////////////////////////////////////////
	// 🔐 0. INPUT VALIDATION
	//////////////////////////////////////////////////////////////

	if userID <= 0 {
		return errors.New("invalid user")
	}

	if amount <= 0 {
		return errors.New("invalid amount")
	}

	//////////////////////////////////////////////////////////////
	// 🔐 1. BEGIN TRANSACTION
	//////////////////////////////////////////////////////////////

	tx, err := db.Begin()
	if err != nil {
		return err
	}
	defer tx.Rollback()

	//////////////////////////////////////////////////////////////
	// ⏱️ 2. CONTEXT TIMEOUT (CRITICAL)
	//////////////////////////////////////////////////////////////

	ctx, cancel := context.WithTimeout(context.Background(), 3*time.Second)
	defer cancel()

	//////////////////////////////////////////////////////////////
	// 🔒 3. LOCK USER ROW + CHECK BALANCE
	//////////////////////////////////////////////////////////////

	var balance float64

	err = tx.QueryRowContext(ctx, `
		SELECT subscription_balance
		FROM users
		WHERE id=$1
		FOR UPDATE
	`, userID).Scan(&balance)

	if err != nil {
		return err
	}

	if balance < amount {
		return errors.New("insufficient balance")
	}

	//////////////////////////////////////////////////////////////
	// ➖ 4. DEDUCT BALANCE (LEDGER SAFE)
	//////////////////////////////////////////////////////////////

	err = DeductSubscription(tx, userID, amount)
	if err != nil {
		return err
	}

	//////////////////////////////////////////////////////////////
	// 🧾 5. CREATE WITHDRAWAL REQUEST
	//////////////////////////////////////////////////////////////

	_, err = tx.ExecContext(ctx, `
		INSERT INTO withdrawals (user_id, amount, status, created_at)
		VALUES ($1,$2,'pending',$3)
	`, userID, amount, time.Now())

	if err != nil {
		return err
	}

	//////////////////////////////////////////////////////////////
	// ✅ 6. COMMIT
	//////////////////////////////////////////////////////////////

	return tx.Commit()
}
